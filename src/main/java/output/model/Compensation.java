package output.model;

public class Compensation {
    public int bonusPaid;
    public double compaRatio;
    public String numberOfCompensation;

    public int getBonusPaid() {
        return bonusPaid;
    }

    public void setBonusPaid(int bonusPaid) {
        this.bonusPaid = bonusPaid;
    }

    public double getCompaRatio() {
        return compaRatio;
    }

    public void setCompaRatio(double compaRatio) {
        this.compaRatio = compaRatio;
    }

    public String getNumberOfCompensation() {
        return numberOfCompensation;
    }

    public void setNumberOfCompensation(String numberOfCompensation) {
        this.numberOfCompensation = numberOfCompensation;
    }
}
